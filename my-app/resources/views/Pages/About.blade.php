               <h2 class="mB-T2B">
                  <div class="field field--name-field-heading field--type-string field--label-hidden field--item">What is a donor-advised fund?</div>
               </h2>
               <div class="color-noneforP common-Font-Color mB-section whole-panel-body clearfix simple-text-cta-body">
                  <div class="field field--name-body field--type-text-with-summary field--label-hidden field--item">
                     <p>A donor-advised fund (DAF) is a charitable giving account designed exclusively to invest, grow, and give assets to charities for meaningful and lasting impact. Here's how it works: You donate assets into a DAF and recommend how those assets should be invested. Your contribution is tax-deductible and the assets in the DAF grow tax-free. When you're ready, <a class="teal-underline" href="/giving-with-vc/how-it-works/granting" target="blank">recommend a grant</a> to a charity you care about, and we take care of the rest. Some people call it a giving vehicle. Others call it a philanthropic fund. We call it the most convenient, tax-efficient way to give back.</p>
